def test_handle_signal_kills_worker_on_done(tmp_path):
    """handle_signal calls cleanup_launch when done/failed/blocked signal is received."""
    # Setup
    queue_dir = tmp_path / "pools" / "work" / "Queue"
    queue_dir.mkdir(parents=True)
    worker1_dir = tmp_path / "pools" / "work" / "worker_01"
    worker1_dir.mkdir()
    (worker1_dir / "workspace").mkdir()
    (tmp_path / "pools" / "work" / "worker_02" / "workspace").mkdir(parents=True)
    (tmp_path / "pools" / "work" / "Outbox").mkdir(parents=True)
    tools_dir = tmp_path / "runtime" / "tools"
    tools_dir.mkdir(parents=True)
    for bat_name in ["Online.bat", "StartWriting.bat", "Done.bat"]:
        (tools_dir / bat_name).write_text(f"@echo off\necho {bat_name}", encoding="utf-8")

    runtime = WorkRuntime(root_dir=tmp_path, signal_port=18812)
    runtime._lifecycle_tools_dir = tools_dir

    import app.shared.launch_manager as lm_module
    original_cleanup = lm_module.LaunchManager.cleanup_launch

    cleanup_calls = []

    def tracked_cleanup(self, launch_result):
        cleanup_calls.append(launch_result)
        return {"cleaned": True, "job_handle": launch_result.get("job_handle")}

    lm_module.LaunchManager.cleanup_launch = tracked_cleanup

    # Mark slot as busy with a launch_result that includes job_handle
    slot = runtime.get_slot("worker_01")
    assert slot is not None
    slot.busy = True
    slot.assigned_task_id = "t_003"
    slot.launch_result = {
        "launched": True,
        "pid": 12345,
        "job_handle": 0xBEEF,
    }

    try:
        # Send done signal
        signal_result = {
            "agent_id": "worker_01",
            "task_id": "t_003",
            "signal": "done",
            "is_terminal": True,
        }

        runtime.handle_signal(signal_result)

        # CRITICAL: cleanup_launch MUST have been called
        assert len(cleanup_calls) == 1, (
            f"cleanup_launch not called. Expected 1 call, got {len(cleanup_calls)}. "
            "handle_signal does not kill worker on terminal signal."
        )

        # The job_handle must be passed through
        assert cleanup_calls[0].get("job_handle") == 0xBEEF, (
            "job_handle not passed to cleanup_launch"
        )

        # Slot should also be released
        assert slot.busy is False
        assert slot.assigned_task_id == ""

    finally:
        lm_module.LaunchManager.cleanup_launch = original_cleanup


