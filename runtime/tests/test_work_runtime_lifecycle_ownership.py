    queue_dir = tmp_path / "pools" / "work" / "Queue"
    queue_dir.mkdir(parents=True)
    worker1_dir = tmp_path / "pools" / "work" / "worker_01"
    worker1_dir.mkdir()
    (worker1_dir / "workspace").mkdir()
    (tmp_path / "pools" / "work" / "worker_02" / "workspace").mkdir(parents=True)
    (tmp_path / "pools" / "work" / "Outbox").mkdir(parents=True)
    # Lifecycle bats source dir
    tools_dir = tmp_path / "runtime" / "tools"
    tools_dir.mkdir(parents=True)
    for bat_name in ["Online.bat", "StartWriting.bat", "Done.bat", "signal_bridge.py", "BOOTSTRAP.txt"]:
        (tools_dir / bat_name).write_text(f"@echo off\necho {bat_name}", encoding="utf-8")

    # Create a task file
    task_file = queue_dir / "task_001.txt"
    task_file.write_text(
        "TASK_ID: t_001\nFEATURE_ID: f_test\nPOOL: work\n\ntest body",
        encoding="utf-8",
    )

    runtime = WorkRuntime(root_dir=tmp_path, signal_port=18810)

    # Inject tools_dir so WorkRuntime can find lifecycle bats
    runtime._lifecycle_tools_dir = tools_dir

    # Mock LaunchManager.launch to avoid real process spawning
    import app.shared.launch_manager as lm_module
    original_launch = lm_module.LaunchManager.launch

    def mock_launch(self, request, dry_run=True):
        return {
            "launched": True,
            "dry_run": dry_run,
            "command": ["cmd"],
            "cwd": str(tmp_path),
            "pid": 9999,
            "job_handle": 0xDEAD,
        }

    lm_module.LaunchManager.launch = mock_launch
    try:
        result = runtime.dispatch_next(dry_run=True)

        assert result["dispatched"] is True

        # Lifecycle bats MUST exist in worker slot directory
        for bat_name in ["Online.bat", "StartWriting.bat", "Done.bat"]:
            bat_path = worker1_dir / bat_name
            assert bat_path.exists(), f"{bat_name} not deployed to worker slot"
            # Content should match source (not empty)
            content = bat_path.read_text(encoding="utf-8")
            assert len(content) > 0, f"{bat_name} deployed but empty"

        # Non-work-pool bats should NOT be deployed
        assert not (worker1_dir / "StartThinking.bat").exists()

    finally:
        lm_module.LaunchManager.launch = original_launch


