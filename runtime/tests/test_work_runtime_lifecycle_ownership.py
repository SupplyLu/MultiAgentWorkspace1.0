    # Track if _ensure_launch_bat was called
    ensure_called = []

    original_ensure = lm_module.LaunchManager._ensure_launch_bat

    def tracked_ensure(self, request):
        ensure_called.append(request.bat_path)
        # DO NOT call original — simulating the fix

